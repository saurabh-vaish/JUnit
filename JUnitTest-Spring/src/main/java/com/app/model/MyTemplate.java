package com.app.model;



public class MyTemplate {

	private Integer sessId;

	public MyTemplate() {
		super();
	}

	public MyTemplate(Integer sessId) {
		super();
		this.sessId = sessId;
	}

	public Integer getSessId() {
		return sessId;
	}

	public void setSessId(Integer sessId) {
		this.sessId = sessId;
	}
	
	
}
